import os
import json
from typing import List


def load_fields() -> List[List[List[str]]]:
    """Loads locations of the game

    Returns
    -------
    List[List[List[str]]]
        List of locations matrix

    Raises
    ------
    ValueError
        If environment variable with map file did not set
    """

    fields: List[List[List[str]]] = []
    file = os.getenv("CVC_MAP")
    if file is None:
        raise ValueError("CVC_MAP environment variable did not set")

    with open(file) as fp:
        all_maps = json.load(fp)
        for id, map in all_maps.items():
            fields.append([[c for c in row if c != "\n"] for row in map])
    return fields
